def extract_match_info(url):
    match_pattern = r'matches/([a-zA-Z0-9-]+)-vs-([a-zA-Z0-9-]+)-(\d{2}-\d{2}-\d{4})/(.+)'
    match = re.search(match_pattern, url)
    if match:
        team1 = match.group(1)
        team2 = match.group(2)
        date = match.group(3)
        map_name = match.group(4)
        return {
            'team1': team1,
            'team2': team2,
            'date': date,
            'map': map_name
        }
    return None

def scrape_team_data(match_url):
    response = requests.get(match_url)
    if response.status_code != 200:
        print(f"Failed to fetch {match_url}")
        return None
    
    soup = BeautifulSoup(response.text, 'html.parser')
    
    # Extract match info from URL
    match_info = extract_match_info(match_url)
    if not match_info:
        print(f"Failed to extract match info from URL: {match_url}")
        return None
    
    # Extract the current map scores directly from the HTML structure visible in the image
    map_score_team1 = "0"
    map_score_team2 = "0"
    map_winner = ""
    
    # Look for the exact map we're interested in
    # The structure shows score-text > score-1 and score-2 elements
    current_map_url = match_url.split('/')[-1]
    
    # Find all match menu items
    match_menu_items = soup.find_all('div', class_='c-nav-match-menu-item--finished')
    
    for menu_item in match_menu_items:
        # Find the link to the map
        map_link = menu_item.find('a', href=lambda href: href and current_map_url in href)
        
        if map_link:
            # This is the menu item for our current map
            # Find the score map within this item or its next sibling
            score_map = menu_item.find('div', class_='c-match-score-map') or menu_item.find_next('div', class_='c-match-score-map')
            
            if score_map:
                score_text = score_map.find('div', class_='score-text')
                if score_text:
                    team1_score = score_text.find('span', class_='score-1')
                    team2_score = score_text.find('span', class_='score-2')
                    
                    if team1_score:
                        map_score_team1 = team1_score.text.strip()
                    
                    if team2_score:
                        map_score_team2 = team2_score.text.strip()
                    
                    # Determine the winner based on the 'winner' class
                    if team1_score and 'winner' in team1_score.get('class', []):
                        map_winner = match_info['team1']
                    elif team2_score and 'winner' in team2_score.get('class', []):
                        map_winner = match_info['team2']
                    else:
                        if map_score_team1 and map_score_team2:
                            # If scores are equal, it's a draw
                            if int(map_score_team1) == int(map_score_team2):
                                map_winner = "Draw"
                            elif int(map_score_team1) > int(map_score_team2):
                                map_winner = match_info['team1']
                            else:
                                map_winner = match_info['team2']
    
    # If we couldn't find the map-specific scores, try to find any score-text in the page
    if map_score_team1 == "0" and map_score_team2 == "0":
        for score_div in soup.find_all('div', class_='score-text'):
            team1_score = score_div.find('span', class_='score-1')
            team2_score = score_div.find('span', class_='score-2')
            
            if team1_score and team2_score:
                map_score_team1 = team1_score.text.strip()
                map_score_team2 = team2_score.text.strip()
                
                # Determine the winner based on the 'winner' class
                if 'winner' in team1_score.get('class', []):
                    map_winner = match_info['team1']
                elif 'winner' in team2_score.get('class', []):
                    map_winner = match_info['team2']
                else:
                    if map_score_team1 and map_score_team2:
                        # If scores are equal, it's a draw
                        if int(map_score_team1) == int(map_score_team2):
                            map_winner = "Draw"
                        elif int(map_score_team1) > int(map_score_team2):
                            map_winner = match_info['team1']
                        else:
                            map_winner = match_info['team2']
                
                # We found scores, break out of the loop
                break
    
    scoreboard_widgets = soup.find_all('div', class_='c-widget-match-scoreboard')
    if len(scoreboard_widgets) < 2:
        print("Could not find two separate scoreboards for teams")
        return None
    
    teams_data = []
    
    # Process each team's scoreboard separately
    for widget in scoreboard_widgets:
        team_info = {}
        
        team_header = widget.find('div', class_='c-widget-header-team')
        if team_header:
            team_name = team_header.find('p').text.strip()
            team_info['team_name'] = team_name
        else:
            continue  # Skip if team name can't be found
        
        table_group = widget.find('div', class_='table-group')
        if not table_group:
            continue
            
        player_rows = table_group.find_all('div', class_='table-row')
        player_rows = [row for row in player_rows if 'total' not in row.get('class', [])]
        
        players = []
        for row in player_rows:
            player_data = {}
            
            player_name_tag = row.find('span', class_='nickname')
            if player_name_tag:
                player_data['player_name'] = player_name_tag.text.strip()
            else:
                continue  # Skip this row if player name is missing
            
            kills_cell = row.find('div', class_='table-cell kills')
            deaths_cell = row.find('div', class_='table-cell deaths')
            assists_cell = row.find('div', class_='table-cell assists')
            adr_cell = row.find('div', class_='table-cell adr')
            
            open_duels_cell = row.find('div', class_='table-cell open-duels')
            multi_kills_cell = row.find('div', class_='table-cell multi-kills')
            
            player_data['kills'] = kills_cell.find('p', class_='value').text.strip() if kills_cell else None
            player_data['deaths'] = deaths_cell.find('p', class_='value').text.strip() if deaths_cell else None
            player_data['assists'] = assists_cell.find('p', class_='value').text.strip() if assists_cell else None
            player_data['adr'] = adr_cell.find('p', class_='value').text.strip() if adr_cell else None
            player_data['open_duels'] = open_duels_cell.find('p').text.strip() if open_duels_cell else None
            player_data['multi_kills'] = multi_kills_cell.find('p').text.strip() if multi_kills_cell else None
            
            if player_data:
                players.append(player_data)
        
        team_info['players'] = players
        teams_data.append(team_info)
    
    # Get series score
    series_score1 = soup.find('span', class_='score-1')
    series_score2 = soup.find('span', class_='score-2')
    series_score_team1 = series_score1.text.strip() if series_score1 else "N/A"
    series_score_team2 = series_score2.text.strip() if series_score2 else "N/A"

    match_data = {
        'match_id': f"{match_info['team1']}-vs-{match_info['team2']}-{match_info['date']}",
        'team1': match_info['team1'],
        'series_score_team1': series_score_team1,
        'team2': match_info['team2'],
        'series_score_team2': series_score_team2,
        'date': match_info['date'],
        'map': match_info['map'],
        'map_score_team1': map_score_team1,
        'map_score_team2': map_score_team2,
        'map_winner': map_winner,
        'teams': teams_data,
    }
    
    return match_data

def scrape_multiple_matches(match_links):
    all_matches_data = []
    matches = {}
    count = 0

    for url in match_links:
        match_info = extract_match_info(url)
        if match_info:
            match_id = f"{match_info['team1']}-vs-{match_info['team2']}-{match_info['date']}"
            if match_id not in matches:
                matches[match_id] = []
            matches[match_id].append(url)
    
    for match_id, urls in matches.items():
        match_maps = []
        for url in urls:
            count += 1
            print(f"Scraping link {count}: {url}")
            # Uncomment the following line if you want to add a delay between requests
            time.sleep(random.randint(3, 6))
            match_data = scrape_team_data(url)
            if match_data:
                match_maps.append(match_data)
        
        if match_maps:
            match_object = {
                'match_id': match_id,
                'team1': match_maps[0]['team1'],
                'team2': match_maps[0]['team2'],
                'date': match_maps[0]['date'],
                'maps': match_maps
            }
            all_matches_data.append(match_object)
    
    return all_matches_data

def flatten_match_data(all_matches_data):
    # Flatten data for easy export to Excel
    rows = []
    for match in all_matches_data:
        match_id = match['match_id']
        team1 = match['team1']
        team2 = match['team2']
        
        for map_data in match['maps']:
            map_name = map_data['map']
            series_score_team1 = map_data['series_score_team1']
            series_score_team2 = map_data['series_score_team2']
            map_score_team1 = map_data['map_score_team1']
            map_score_team2 = map_data['map_score_team2']
            map_winner = map_data['map_winner']
            
            for team in map_data['teams']:
                team_name = team['team_name']
                for player in team['players']:
                    player_data = {
                        'match_id': match_id,
                        'team1': team1,
                        'series_score_team1': series_score_team1,
                        'team2': team2,
                        'series_score_team2': series_score_team2,
                        'map': map_name,
                        'map_score_team1': map_score_team1,
                        'map_score_team2': map_score_team2,
                        'map_winner': map_winner,
                        'team_name': team_name,
                        'player_name': player['player_name'],
                        'kills': player['kills'],
                        'deaths': player['deaths'],
                        'assists': player['assists'],
                        'adr': player['adr'],
                        'open_duels': player['open_duels'],
                        'multi_kills': player['multi_kills']
                    }
                    rows.append(player_data)
    return rows

